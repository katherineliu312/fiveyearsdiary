export const AnnualRingGraph: React.FC<{ count: number; className?: string }> = ({ count, className }) => {
  // Use a fixed max radius to ensure consistency
  // A day with 1 entry has 1 small ring.
  // A day with 5 entries has 5 rings, filling the space.
  
  const rings = [];
  const cx = 12;
  const cy = 12;
  const maxR = 10;
  
  // Strategy: Fixed step size of ~2px to make 'growth' evident.
  // R = 3, 5, 7, 9, 11...
  // If count is very high (> 5), we condense slightly to fit, but typical 5-year use case fits perfectly.
  
  const effectiveCount = Math.min(count, 12); // Visual cap
  const step = effectiveCount > 5 ? (maxR - 2) / (effectiveCount - 1) : 2;
  
  for (let i = 0; i < effectiveCount; i++) {
    rings.push(3 + i * step);
  }

  return (
    <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" className={className}>
      {/* Central seed */}
      <circle cx={cx} cy={cy} r="1" fill="currentColor" className="opacity-80" />
      
      {/* Rings */}
      {rings.map((r, i) => (
        <circle 
          key={i} 
          cx={cx} 
          cy={cy} 
          r={r} 
          strokeWidth={1.5}
          className="opacity-70"
        />
      ))}
    </svg>
  );
};